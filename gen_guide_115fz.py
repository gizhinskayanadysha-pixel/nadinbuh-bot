DARK  = RGBColor(0x3C, 0x11, 0x0F)
RED   = RGBColor(0x7D, 0x17, 0x13)
GRAY  = RGBColor(0x80, 0x80, 0x80)
BLACK = RGBColor(0x1A, 0x1A, 0x1A)

def h1(doc, text):
    p = doc.add_paragraph()
    r = p.add_run(text)
    r.bold = True
    r.font.size = Pt(16)
    r.font.color.rgb = RED
    p.paragraph_format.space_before = Pt(14)
    p.paragraph_format.space_after  = Pt(6)
    return p

def h2(doc, text):
    p = doc.add_paragraph()
    r = p.add_run(text)
    r.bold = True
    r.font.size = Pt(13)
    r.font.color.rgb = DARK
    p.paragraph_format.space_before = Pt(10)
    p.paragraph_format.space_after  = Pt(4)
    return p

def body(doc, text):
    p = doc.add_paragraph(text)
    p.runs[0].font.size = Pt(11)
    p.runs[0].font.color.rgb = BLACK
    p.paragraph_format.space_after = Pt(3)
    return p

def bullet(doc, text):
    p = doc.add_paragraph(text, style="List Bullet")
    p.runs[0].font.size = Pt(11)
    p.runs[0].font.color.rgb = BLACK
    p.paragraph_format.space_after = Pt(2)
    return p

def divider(doc):
    p = doc.add_paragraph("─" * 75)
    p.runs[0].font.size = Pt(9)
    p.runs[0].font.color.rgb = GRAY
    p.paragraph_format.space_before = Pt(6)
    p.paragraph_format.space_after  = Pt(6)

def template_block(doc, title, body_lines):
    p = doc.add_paragraph()
    r = p.add_run(title)
    r.bold = True
    r.font.size = Pt(11)
    r.font.color.rgb = DARK
    p.paragraph_format.space_before = Pt(8)
    p.paragraph_format.space_after  = Pt(2)
    for line in body_lines:
        p2 = doc.add_paragraph(line)
        if p2.runs:
            p2.runs[0].font.size = Pt(10)
            p2.runs[0].font.color.rgb = BLACK
            p2.runs[0].font.italic = True
        p2.paragraph_format.space_after = Pt(1)
        p2.paragraph_format.left_indent = Cm(0.7)


